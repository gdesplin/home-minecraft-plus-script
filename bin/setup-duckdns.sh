#!/usr/bin/env bash
# setup-duckdns.sh — Install DuckDNS dynamic DNS updater as a systemd service + timer
# Idempotent: safe to run multiple times.
# Requires: duckdns/.env with DUCKDNS_TOKEN and DUCKDNS_SUBDOMAIN set.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"
ENV_FILE="${REPO_ROOT}/duckdns/.env"

info()  { echo "  [INFO]  $*"; }
ok()    { echo "  [ OK ]  $*"; }
warn()  { echo "  [WARN]  $*"; }
err()   { echo "  [ERR]   $*" >&2; }

require_root() {
  if [[ "${EUID}" -ne 0 ]]; then
    echo "ERROR: Run this script with sudo or as root." >&2
    exit 1
  fi
}

require_root

echo ""
echo "============================================"
echo "  setup-duckdns.sh — DuckDNS DDNS updater"
echo "============================================"
echo ""

# ── Load env file ─────────────────────────────────────────────────────────────
if [[ ! -f "${ENV_FILE}" ]]; then
  err "Missing env file: ${ENV_FILE}"
  err "Copy duckdns/.env.example to duckdns/.env and fill in your credentials."
  exit 1
fi

# shellcheck source=/dev/null
source "${ENV_FILE}"

if [[ -z "${DUCKDNS_TOKEN:-}" || "${DUCKDNS_TOKEN}" == "CHANGE_ME"* ]]; then
  err "DUCKDNS_TOKEN is not set or still has the placeholder value."
  err "Edit ${ENV_FILE} and set your real DuckDNS token."
  exit 1
fi

if [[ -z "${DUCKDNS_SUBDOMAIN:-}" || "${DUCKDNS_SUBDOMAIN}" == "CHANGE_ME"* ]]; then
  err "DUCKDNS_SUBDOMAIN is not set or still has the placeholder value."
  err "Edit ${ENV_FILE} and set your DuckDNS subdomain (e.g. 'myhomemc')."
  exit 1
fi

info "Using subdomain: ${DUCKDNS_SUBDOMAIN}.duckdns.org"

# ── Install curl (if missing) ─────────────────────────────────────────────────
if ! command -v curl &>/dev/null; then
  apt-get install -y -qq curl
fi

# ── Install the updater script ────────────────────────────────────────────────
DUCKDNS_SCRIPT="/usr/local/bin/duckdns-update.sh"
info "Writing DuckDNS updater script to ${DUCKDNS_SCRIPT}..."
cat > "${DUCKDNS_SCRIPT}" <<SCRIPT
#!/usr/bin/env bash
# Auto-generated by setup-duckdns.sh — do not edit by hand.
# Update DuckDNS with the current public IP.
set -euo pipefail

SUBDOMAIN="${DUCKDNS_SUBDOMAIN}"
TOKEN="${DUCKDNS_TOKEN}"
LOG="/var/log/duckdns.log"

RESPONSE=\$(curl -fsSL "https://www.duckdns.org/update?domains=\${SUBDOMAIN}&token=\${TOKEN}&ip=" 2>&1)
TIMESTAMP=\$(date '+%Y-%m-%d %H:%M:%S')
echo "\${TIMESTAMP}  \${RESPONSE}" >> "\${LOG}"

if [[ "\${RESPONSE}" == "OK"* ]]; then
  exit 0
else
  echo "\${TIMESTAMP}  ERROR: unexpected response: \${RESPONSE}" >&2
  exit 1
fi
SCRIPT
chmod 755 "${DUCKDNS_SCRIPT}"
ok "Updater script installed."

# ── Create log file ───────────────────────────────────────────────────────────
touch /var/log/duckdns.log
chmod 644 /var/log/duckdns.log

# ── Install systemd service ───────────────────────────────────────────────────
SERVICE_FILE="/etc/systemd/system/duckdns.service"
info "Writing systemd service unit..."
cat > "${SERVICE_FILE}" <<'EOF'
[Unit]
Description=DuckDNS dynamic DNS update
After=network-online.target
Wants=network-online.target

[Service]
Type=oneshot
ExecStart=/usr/local/bin/duckdns-update.sh
StandardOutput=journal
StandardError=journal
EOF
ok "Service unit written."

# ── Install systemd timer ─────────────────────────────────────────────────────
TIMER_FILE="/etc/systemd/system/duckdns.timer"
info "Writing systemd timer unit (every 5 minutes)..."
cat > "${TIMER_FILE}" <<'EOF'
[Unit]
Description=DuckDNS dynamic DNS update timer

[Timer]
OnBootSec=1min
OnUnitActiveSec=5min
Persistent=true

[Install]
WantedBy=timers.target
EOF
ok "Timer unit written."

# ── Reload systemd and start timer ───────────────────────────────────────────
info "Enabling and starting DuckDNS timer..."
systemctl daemon-reload
systemctl enable --now duckdns.timer
ok "DuckDNS timer enabled."

# ── Run an immediate update ───────────────────────────────────────────────────
info "Running an immediate DuckDNS update..."
systemctl start duckdns.service
sleep 2
LAST_LOG=$(tail -1 /var/log/duckdns.log 2>/dev/null || echo "(no log yet)")
ok "Last update: ${LAST_LOG}"

echo ""
echo "✓ DuckDNS setup complete."
echo ""
echo "  Your hostname: ${DUCKDNS_SUBDOMAIN}.duckdns.org"
echo "  Timer updates every 5 minutes."
echo ""
echo "  Useful commands:"
echo "    sudo systemctl status duckdns.timer"
echo "    tail -f /var/log/duckdns.log"
echo ""
echo "  NEXT STEPS:"
echo "    Edit minecraft/.env then:"
echo "    sudo bash ${REPO_ROOT}/bin/setup-minecraft.sh"
echo ""
